function pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

edit1 = str2double(get(handles.edit1,'String')); %mengambil isi box pH Value
edit2 = str2double(get(handles.edit2,'String')); %mengambil isi box Hardness
edit3 = str2double(get(handles.edit3,'String')); %mengambil isi box Solids
edit4 = str2double(get(handles.edit4,'String')); %mengambil isi box Chloramines
edit5 = str2double(get(handles.edit5,'String')); %mengambil isi box Sulfate
edit6 = str2double(get(handles.edit6,'String')); %mengambil isi box Conductivity
edit7 = str2double(get(handles.edit7,'String')); %mengambil isi box Organic Carbon
edit8 = str2double(get(handles.edit8,'String')); %mengambil isi box Trihalomethanes
edit9 = str2double(get(handles.edit9,'String')); %mengambil isi box Turbidity
edit10 = str2double(get(handles.edit10,'String')); %mengambil isi box K

sample = [edit1 edit2 edit3 edit4 edit5 edit6 edit7 edit8 edit9]; %memasukkan isi-isi di atas ke dalam sample

opts = detectImportOptions('water_potability.csv'); %import file .csv dengan nama water_potability.csv
opts.SelectedVariableNames = (1:9); %mengambil isi dari kolom 1 sampai 9
training = readmatrix('water_potability.csv', opts); %menyimpan isi dari kolom 1 sampai 9 ke dalam training

opts = detectImportOptions('water_potability.csv'); %import file .csv dengan nama water_potability.csv
opts.SelectedVariableNames = (10); %mengambil isi dari kolom 10
group = readmatrix('water_potability.csv', opts); %menyimpan isi dari kolom 10 ke dalam group

%proses klasifikasi dengan KNN
class = fitcknn(training, group, 'NumNeighbors', edit10);
klasifikasi = predict(class, sample);

set(handles.text14,'String', klasifikasi); %menampilkan hasil klasifikasi ke dalam text14 (Result)

